    public function registSeat(string $seat_str)
    {
        $seat = Seat::where('code', $seat_str)->first();
        if (!$seat || !$seat->enabled) {
            return abort(404);
        }
        $user = \Auth::user();
        $record = RegistRecord::createRecord($user, $seat);
        return view('seats.regist_seat', compact('seat', 'user', 'record'));
    }
